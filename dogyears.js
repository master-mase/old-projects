// The difference in aging between humans and dogs
const ageDifference = 45 / 8
// Current age
const myAge = 26
// Calculating my dog age from my human age
const myDogAge = myAge / ageDifference

console.log(`Being ${myAge} years old at the moment, I would be ${myDogAge.toFixed(1)} years old as a dog`)
// Being 26 years old at the moment, I would be 4.622222222222222 years old as a dog
